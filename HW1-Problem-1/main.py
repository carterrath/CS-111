#Assign 0.078 to a variable for tax rate
tax_rate = 0.078
#Assign 10 to a variable for the number of sets
number_of_sets = 10
#Assign 4.5 to a variable for the cost of each set
cost_per_set = 4.5
#Determine the cost of all sets and store it in a variable
cost_of_sets = number_of_sets * cost_per_set
#Determine the amount of tax and store it in a variable
tax_amount = cost_of_sets * tax_rate
#Display the amount of tax
print ("$", tax_amount, "tax collected")